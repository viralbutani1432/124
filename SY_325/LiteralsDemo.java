public class LiteralsDemo
{
	public static void main(String []args)
	{
		int decimalLiteral=100;
		int binaryLiteral=011100100;
		int octalLiteral=0144;
		int hexLiteral=0xb4;
		
		float floatLiteral=2.710f;
		char charLiteral='A';
		String stringLiteral="kayum";
		
		boolean trueLiteral=true;
		boolean falseLiteral=false;
		
		String milLiteral=null;
		
		System.out.println("Integer Literal=");
		System.out.println("Demaic="+decimalLiteral+"\n");
		System.out.println("binary="+binaryLiteral+"\n");
		System.out.println("octal="+octalLiteral+"\n");
		System.out.println("hex="+hexLiteral+"\n");
		
		System.out.println("float="+floatLiteral+"\n");
		System.out.println("char="+charLiteral+"\n");
		
		System.out.println("string="+stringLiteral+"\n");
		
		System.out.println("true="+trueLiteral+"\n");
		System.out.println("false="+falseLiteral+"\n");
		System.out.println("string="+milLiteral+"\n");
	}
}